import { ONENODE_API_BASE_URL } from '$lib/constants';


export const getServiceUrl = async (token: string = '') => {
	let error = null;

	const res = await fetch(`${ONENODE_API_BASE_URL}/configs/serviceUrl`, {
		method: 'GET',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			...(token && { authorization: `Bearer ${token}` })
		}
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			console.log(err);
			if ('detail' in err) {
				error = err.detail;
			} else {
				error = 'Server connection failed';
			}
			return null;
		});

	if (error) {
		throw error;
	}

	return res;
};

export const updateServiceUrl = async (token: string = '', serviceUrl: object) => {
	let error = null;

	const res = await fetch(`${ONENODE_API_BASE_URL}/configs/serviceUrl/update`, {
		method: 'POST',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			...(token && { authorization: `Bearer ${token}` })
		},
		body: JSON.stringify(serviceUrl)
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			console.log(err);
			if ('detail' in err) {
				error = err.detail;
			} else {
				error = 'Server connection failed';
			}
			return null;
		});
	if (error) {
		throw error;
	}

	return res;
};

